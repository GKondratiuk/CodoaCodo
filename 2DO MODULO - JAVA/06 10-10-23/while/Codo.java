/*
 * Trabajamos con Bucles.
 * Imprimimos por consola los numeros del 0 al 100 
 * mediante un bucle while 
 * Ponemos en practica la utilizacion de variables y acumuladores
 * Aprenderemos a declarar un bucle while
 */

public class Codo{
public static void main(String args[]){
//DECLARACION E INICIALIZACION DE LAS VARIABLES
	int contador = 0;
//PROCESO MEDIANTE BUCLE WHILE
	while(contador <= 100){
		//cuerpo del bucle
		System.out.println(contador);
		//Actualizacion de la variable del contador
		//contador = contador +1;
		contador ++;
	}
	
	
	
 }
}
