public class MiArray{
	public static void main(String args[]){
		
		//DECLARACION DE UN ARRAY 
		int[] numeros;
		
		//INSTANCIACION
		numeros = new int[3];
		
		//INICIALIZACION
		numeros[0] = 3; // es el elemento 1 del indice 0
		numeros[1] = 5;
		numeros[2] = 459;
		
		//INSTRUCCION DE SALIDA
		System.out.println("El elemento 2 del indice 1 es: " + numeros[1]);
		}
	}
